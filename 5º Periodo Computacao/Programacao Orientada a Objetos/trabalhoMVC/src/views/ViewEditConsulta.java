    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        String data = txtEditData.getText();
        int id_paciente = Integer.parseInt(txtEditId.getText());
        
        ConsultaController cc = new ConsultaController(data, id_paciente);

        String result = cc.editar(this.id);

        if(result != null)
            JOptionPane.showMessageDialog(rootPane, result);
        else
            JOptionPane.showMessageDialog(rootPane, "Houve algum erro, tente novamente!");

        this.dispose();
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void txtEditDataAncestorAdded(javax.swing.event.AncestorEvent evt) {//GEN-FIRST:event_txtEditDataAncestorAdded
        ResultSet rs = new ConsultaController().visualizar(this.id);

        try {
            while(rs.next()) {
                txtEditData.setText(rs.getString("data"));
                txtEditId.setText(rs.getString("paciente_id"));
            }
        } catch(SQLException ex) {
            System.out.println(ex);
            JOptionPane.showMessageDialog(rootPane, "Houve algum erro de conex√£o, tente novamente!");
        }
    }//GEN-LAST:event_txtEditDataAncestorAdded

